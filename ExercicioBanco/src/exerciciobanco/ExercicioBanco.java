    private void btnEncerrarContaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEncerrarContaActionPerformed
        // TODO add your handling code here:
        // Chamando método fechar conta:
        p1.fecharConta();
        
        if(p1.isStatus() == false) {
            // Remove texto do label do usuário:
            lblNomeCliente.setText("");
            
            // Remove icone do label de imagem:
            lblImagem.setIcon(null);

            // Se a conta foi fechada, desativa botões de operações bancárias:
            btnInfo.setEnabled(false);
            btnDeposito.setEnabled(false);
            btnSaque.setEnabled(false);
            btnPagarMensalidade.setEnabled(false);
            btnEncerrarConta.setEnabled(false);
        
            // Ativa botão de criar conta, caso a pessoa queira criar novamente:
            btnCriarConta.setEnabled(true);
        }
    }//GEN-LAST:event_btnEncerrarContaActionPerformed

