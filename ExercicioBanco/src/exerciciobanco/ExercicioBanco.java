    private void btnCriarContaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCriarContaActionPerformed
        // TODO add your handling code here:
        // Strings para nome do cliente e tipo de conta:
        String nomeCliente = new String();
        String tipoConta = new String();
        
        // Pedindo tipo de conta para usuário, podendo ser apenas CC ou CP:
        do {
            tipoConta = JOptionPane.showInputDialog(null, "Insira o tipo de conta (CC ou CP):");
            if(!tipoConta.equals("CC") && !tipoConta.equals("CP")) {
                JOptionPane.showMessageDialog(null, "Por favor, insira uma opção válida!");
            }
        } while (!tipoConta.equals("CC") && !tipoConta.equals("CP"));
        
        // Criando conta:
        p1.abrirConta(tipoConta);
        
        // Pedindo nome para usuário:
        nomeCliente = JOptionPane.showInputDialog(null, "Insira o seu nome:");
        p1.setNomeCliente(nomeCliente);
        
        // Exibindo mensagem de sucesso:
        JOptionPane.showMessageDialog(null, "Conta criada com sucesso!");
        
        // Desativando botão de criar conta:
        btnCriarConta.setEnabled(false);
        
        // Exibindo nome do cliente no label respectivo:
        lblNomeCliente.setText("Usuário: " + nomeCliente);
        
        // Exibindo icone no label de imagem:
        lblImagem.setIcon(new javax.swing.ImageIcon(getClass().getResource("/imagens/iconepessoa.png")));
        
        // Ativando botões de operações bancárias:
        btnInfo.setEnabled(true);
        btnDeposito.setEnabled(true);
        btnSaque.setEnabled(true);
        btnPagarMensalidade.setEnabled(true);
        btnEncerrarConta.setEnabled(true);
    }//GEN-LAST:event_btnCriarContaActionPerformed

